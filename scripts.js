
function menulogin(){
   let menu = document.getElementById('registro')

   if(menu.style.display  == 'none'){
    menu.style.display = 'block'
   } else{
    menu.style.display = 'none'
   }
    
}

function sumirmenu(){
    let menu = document.getElementById('registro')
    menu.style.display = 'none'
}